<script setup lang="ts">
import imports from "@/utils/imports";
import sidebar from "../ui/sidebar.vue";

const { ref, client } = imports();

const isOpen = ref(false);

const open = () => {
    if (document.body.classList.contains("overflow-hidden"))
        document.body.classList.remove("overflow-hidden");
    else document.body.classList.add("overflow-hidden");

    isOpen.value = !isOpen.value;
};

document.addEventListener("click", function (event) {
    const target = event.target as HTMLElement;
    const overlay = document.getElementById("overlay");
    const shoppingCart = document.getElementById("shoppingCart");
    const shoppingCartButton = document.getElementById("shoppingCartButton");

    if (
        !(shoppingCart?.contains(target) || shoppingCartButton?.contains(target)) ||
        (overlay?.contains(target) && isOpen.value)
    ) {
        document.body.classList.remove("overflow-hidden");

        return (isOpen.value = false);
    }
});
</script>
